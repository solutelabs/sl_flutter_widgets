/// [SLButtonType] is used to change the type of widgets
enum SLButtonType {
  /// Default type is [SLButtonType.solid], used to fill with color for widget
  solid,

  /// Type [SLButtonType.outline], used for widget with outline border and fill 
  /// color with Colors.transparent
  outline,

  /// Type [SLButtonType.outline2x], used for widget with outline2x border and 
  /// border.width = 2.0 and fill color with Colors.transparent
  outline2x,

  /// Type [SLButtonType.transparent], used for widget with fill color with
  ///  Colors.transparent
  transparent,
}
